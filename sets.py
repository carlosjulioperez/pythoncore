# Set = Conjunto

# Cast the following list to a set:
print(['A','B','C','A','B','C'])
print(set(['A','B','C','A','B','C']))
# ['A', 'B', 'C', 'A', 'B', 'C']
# Set hasn't permit duplicates
# {'C', 'B', 'A'}

# Add the string 'D' to the set S.
S={'A','B','C'}
S.add('D')
print(S)
# {'D', 'B', 'C', 'A'}